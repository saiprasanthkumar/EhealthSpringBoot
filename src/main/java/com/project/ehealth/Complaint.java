package com.project.ehealth;

public class Complaint {
	private String complaintName;
	private String complaintInfo;
	private String replyComplaint;
	
	public String getComplaintInfo() {
		return complaintInfo;
	}
	public void setComplaintInfo(String complaintInfo) {
		this.complaintInfo = complaintInfo;
	}
	public String getReplyComplaint() {
		return replyComplaint;
	}
	public void setReplyComplaint(String replyComplaint) {
		this.replyComplaint = replyComplaint;
	}
	public String getComplaintName() {
		return complaintName;
	}
	public void setComplaintName(String complaintName) {
		this.complaintName = complaintName;
	}
}
